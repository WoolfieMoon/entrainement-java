public class MiroirDuDoute {
    public static void main(String[] args) {
        boolean aCourage = false;
        boolean aSagesse = true;

        if (!aCourage && !aSagesse) {
            System.out.println("Le miroir reflète ton indécision. Tu dois revenir plus tard.");
            return;
        }

        System.out.println("Le miroir s’incline et te laisse passer.");
    }
}
