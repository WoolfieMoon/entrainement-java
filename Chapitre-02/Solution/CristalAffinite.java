public class CristalAffinite {
    public static void main(String[] args) {
        String affinite = "eau";

        if (affinite.equals("feu")) {
            System.out.println("Le cristal flamboie d’un rouge ardent !");
        } else if (affinite.equals("eau")) {
            System.out.println("Le cristal luit d’un bleu apaisant.");
        } else {
            System.out.println("Le cristal verdit comme la mousse ancienne.");
        }
    }
}
